class Bheem extends Pandava {
    @Override
    void fight() {
        System.out.println("Bheem fights with immense physical strength.");
    }

    @Override
    void obey() {
        System.out.println("Bheem obeys his elders, though he is known for his fiery temperament.");
    }

    @Override
    void kind() {
        System.out.println("Bheem is kind but not as gentle as Yudhishthira.");
    }
}