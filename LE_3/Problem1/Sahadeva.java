class Sahadeva extends Pandava {
    @Override
    void fight() {
        System.out.println("Sahadeva fights with intelligence and foresight.");
    }

    @Override
    void obey() {
        System.out.println("Sahadeva obeys his elders and is the most calm of all Pandavas.");
    }

    @Override
    void kind() {
        System.out.println("Sahadeva is kind and has a deep sense of duty.");
    }
}